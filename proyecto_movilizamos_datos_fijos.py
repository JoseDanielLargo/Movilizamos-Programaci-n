def programar_recorridos(recorridos, conductores_normales, conductores_flotas):
    recorridos_programados = []
    cantidad_recorridos_conductor = []

    for conductor in conductores_normales:
        cantidad_recorridos_conductor.append([conductor[1], 0])

    for recorrido in recorridos:
        ciudad_origen = recorrido[3]
        ciudad_destino = recorrido[8]

        if ciudad_origen == ciudad_destino:
            tipo_recorrido = "Urbano (" + ciudad_origen + "-" + ciudad_destino + ")"
        else:
            tipo_recorrido = "Intermunicipal (" + ciudad_origen + "-" + ciudad_destino + ")"

        conductor_asignado = buscar_conductor_flota(recorrido, conductores_flotas)

        if conductor_asignado is None and recorrido[2] == "Silla Neurologica - Vehiculo Rampa":
            conductor_asignado = buscar_conductor_rampa(recorrido,conductores_normales,cantidad_recorridos_conductor)

            if conductor_asignado is not None:
                aumentar_cantidad_recorridos(conductor_asignado[1],cantidad_recorridos_conductor)

        if conductor_asignado is None:
            conductor_asignado = buscar_conductor_normal(recorrido,conductores_normales,cantidad_recorridos_conductor)

            if conductor_asignado is not None:
                aumentar_cantidad_recorridos(conductor_asignado[1],cantidad_recorridos_conductor)

        if conductor_asignado is not None:
            recorrido_programado = [recorrido[0],recorrido[1],recorrido[2],tipo_recorrido,recorrido[3],recorrido[8],recorrido[6],conductor_asignado[0],conductor_asignado[1],conductor_asignado[13]]
        else:
            recorrido_programado = [recorrido[0],recorrido[1],recorrido[2],tipo_recorrido,recorrido[3],recorrido[8],recorrido[6],"NO ASIGNADO","NO ASIGNADO", "NO ASIGNADO"]

