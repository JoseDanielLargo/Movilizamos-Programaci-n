def buscar_paciente(nombre_paciente, pacientes):
    for paciente in pacientes:
        if paciente[1] == nombre_paciente:
            return paciente

    return None


def buscar_conductor(nombre_conductor, conductores):
    for conductor in conductores:
        if conductor[0] == nombre_conductor:
            return conductor

    return None


def conductores_que_pueden_llevar_paciente(nombre_paciente, pacientes, conductores):
    paciente = buscar_paciente(nombre_paciente, pacientes)

    conductores_posibles = []

    if paciente != None:
        condicion_paciente = paciente[2]

        for conductor in conductores:
            condiciones_aceptadas = conductor[5].split(", ")

            if condicion_paciente in condiciones_aceptadas:
                conductores_posibles.append(conductor)

    return conductores_posibles


def municipios_donde_puede_laborar(conductor):
    municipios = conductor[6]
    lista_municipios = municipios.split(", ")

    return lista_municipios


def hora_a_minutos(hora):
    partes = hora.split(":")
    horas = int(partes[0])
    minutos = int(partes[1])

    return horas * 60 + minutos


def posibles_servicios_compartidos(nombre_paciente, pacientes):
    paciente = buscar_paciente(nombre_paciente, pacientes)

    candidatos = []

    if paciente != None:

        for otro_paciente in pacientes:

            if paciente[0] != otro_paciente[0]:

                misma_ciudad_origen = paciente[3] == otro_paciente[3]
                misma_ciudad_destino = paciente[8] == otro_paciente[8]

                hora_paciente = hora_a_minutos(paciente[6])
                hora_otro_paciente = hora_a_minutos(otro_paciente[6])

                diferencia_hora = abs(hora_paciente - hora_otro_paciente)

                if misma_ciudad_origen and misma_ciudad_destino and diferencia_hora <= 30:
                    candidatos.append(otro_paciente)

    return candidatos



def buscar_conductor_flota(recorrido, conductores_flotas):
    for conductor in conductores_flotas:
        municipios = municipios_donde_puede_laborar(conductor)
        puede_trabajar = recorrido[3] in municipios or recorrido[8] in municipios
        puede_llevar = recorrido[2] in conductor[5].split(", ")
        if puede_trabajar and puede_llevar:
            return conductor
    return None


